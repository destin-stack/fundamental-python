# Re-write book reading's program using while loop

print("=====================================================")
print("                  READING BOOK PROGRAM              ")
books = 10
read_book = 0

while read_book < books:
    print(f"Book {read_book+1} has been read")
    read_book += 1

print("Finish reading all books")
print("=====================================================")
