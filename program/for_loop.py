print("=====================================================")
print("                  READING BOOK PROGRAM              ")
books = 10
read_book = 0

print("Let's read books and get bunch of knowledge!!!")
for i in range(0, books):
    print(f"Book {i+1} has been read")
    read_book += 1

print("Finish reading all books")
